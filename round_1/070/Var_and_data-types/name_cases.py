"""
Name Cases: 
Store a person’s name in a variable, and then print that person’s name in lowercase, uppercase, and titlecase.
"""

name = "firdaus hassan salim saleh"
print("{} in uppercase is {}".format(name, name.upper()))
print("{} in lowercase is {}".format(name, name.lower()))
print("{} in title case is {}".format(name, name.title()))